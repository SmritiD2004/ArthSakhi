# ---------- Translation Functions ----------
def translate_text(text, target_language="English"):
    try:
        if target_language == "English":
            return text
        lang_code = LANGUAGES.get(target_language, "en")
        if lang_code == "en":
            return text
        max_chunk_size = 4000
        if len(text) <= max_chunk_size:
            translator = GoogleTranslator(source="en", target=lang_code)
            return translator.translate(text)
        paragraphs = text.split("\n\n")
        translated_paragraphs = []
        for para in paragraphs:
            if para.strip():
                translator = GoogleTranslator(source="en", target=lang_code)
                translated_para = translator.translate(para)
                translated_paragraphs.append(translated_para)
            else:
                translated_paragraphs.append("")
        return "\n\n".join(translated_paragraphs)
    except Exception:
        return text

def translate_to_english(text, source_language="English"):
    try:
        if source_language == "English":
            return text
        lang_code = LANGUAGES.get(source_language, "en")
        if lang_code == "en":
            return text
        translator = GoogleTranslator(source=lang_code, target="en")
        return translator.translate(text)
    except Exception:
        return text

# ---------- AI Response Function ----------
def get_english_response(prompt: str) -> str:
    if not GROQ_API_KEY:
        return "The service is temporarily unavailable because the AI key (Groq) is not configured. Please configure your GROQ_API_KEY secret."

    system_prompt = (
        "You are ArthSakhi, a friendly financial advisor for women in India. "
        "Use clear, simple language and keep answers focused on the exact question. "
        "If the user asks for a definition like 'What is SIP?', give a short, direct definition first, "
        "then 2тАУ3 practical points, with amounts in Indian Rupees when relevant. "
    )
    
    try:
        headers = {
            "Authorization": f"Bearer {GROQ_API_KEY}",
            "Content-Type": "application/json",
        }
        payload = {
            "model": GROQ_MODEL, 
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": prompt}
            ],
            "max_tokens": 250,
            "temperature": 0.7,
        }
        
        response = requests.post(GROQ_API_URL, headers=headers, json=payload, timeout=60) 
        
        if response.status_code == 200:
            result = response.json()
            if result.get('choices') and result['choices'][0].get('message'):
                text = result['choices'][0]['message']['content'].strip()
                if text:
                    return text
        
        st.error(f"Groq API Error Status: {response.status_code}. Response: {response.text}")
        return "Sorry, the Groq API returned an error. Please check the console for details."
        
    except Exception as e:
        print(f"CRITICAL API EXCEPTION: {e}")
        st.error("CRITICAL: Request failed. Please check your network connection or the console for details.")
        return "Sorry, I am facing a technical issue and cannot answer right now. Please try again later."

