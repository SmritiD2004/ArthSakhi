def get_scheme_info(state, language="English"):
    if state and state != t("select_state_default", language):
        state_schemes = schemes_df[schemes_df["state"] == state]
        if not state_schemes.empty:
            response = f"{t('schemes_in', language)} {state}:\n\n"
            for _, scheme in state_schemes.iterrows():
                scheme_name = translate_text(scheme["scheme"], language)
                scheme_desc = translate_text(scheme["description"], language)
                response += f"{scheme_name}\n{scheme_desc}\n\n"
            response += f"\n{t('visit_office', language)}"
            return response
    return t("select_state_msg", language)

